# Bayes Theorem:
#
# P(A|B) = P(B|A)*P(A) / P(B)
# where
# P(B) = SUM_i(P(B|A_i)*P(A_i))
#
# Generic explanation:
# Probability of A given B is the probability of B given A, multiplied by the prior belief distribution P(A), divided by the probability of B.
#
# Localization explanation:
# Probability of being in state A given measurement B is the probability of seeing measurement B given state A, multiplied by the prior state belief
# distribution P(A), divided by a normalization constant.
